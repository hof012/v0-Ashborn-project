// Placeholder image URL to use as a last resort fallback
const PLACEHOLDER_IMAGE = "/placeholder.svg?height=48&width=48"

// Get sprite for a specific animation frame
export function getPlayerSprite(animation: string, frame: number): string {
  const sprites = PLAYER_SPRITES[animation as keyof typeof PLAYER_SPRITES]
  if (!sprites || sprites.length === 0) {
    return PLACEHOLDER_IMAGE // Default fallback
  }

  // Loop through available frames
  return sprites[frame % sprites.length]
}

// Get all player sprites as a flat array (useful for preloading)
export function getAllPlayerSprites(): string[] {
  const allSprites: string[] = []

  Object.values(PLAYER_SPRITES).forEach((sprites) => {
    if (Array.isArray(sprites)) {
      sprites.forEach((sprite) => {
        if (!allSprites.includes(sprite)) {
          allSprites.push(sprite)
        }
      })
    }
  })

  return allSprites
}
